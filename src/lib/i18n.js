export const t = derived(preferences, ($prefs) => {
  const lang = $prefs.lang || 'no'
  return (key) => translations[lang]?.[key] ?? translations.no[key] ?? key
})

export function getLocalizedName(nameObj, lang) {
  if (!nameObj) return ''
  return nameObj[lang] ?? nameObj.no ?? ''
}
